function Jm = jacobianForte(t)
  
    % calcula o Jacobiano a partir das expressï¿½es abaixo e usando os
    % angulos em t, angulos em graus.

    t1 = deg2rad(t(1)); t2 = deg2rad(t(2)); t3 = deg2rad(t(3));
    t4 = deg2rad(t(1)); t5 = deg2rad(t(5)); % t6 = (t(6)*pi)/180;

    J(1,1) = round(650*sin(t1)*sin(t2)*sin(t3) - 105*cos(t2)*sin(t1)*sin(t3) ...
           - 105*cos(t3)*sin(t1)*sin(t2) - 220*cos(t1)*sin(t4)*sin(t5) ...
           - 525*sin(t1)*sin(t2) - 220*cos(t2 + t3)*cos(t5)*sin(t1) ...
           - 650*cos(t2)*cos(t3)*sin(t1) + 220*cos(t2)*cos(t4)*sin(t1)*sin(t3)*sin(t5) ...
           + 220*cos(t3)*cos(t4)*sin(t1)*sin(t2)*sin(t5));

    J(1,2) = round(-5*cos(t1)*(130*cos(t2)*sin(t3) - 21*cos(t2)*cos(t3) ...
           - 105*cos(t2) + 130*cos(t3)*sin(t2) + 21*sin(t2)*sin(t3) ...
           + 44*sin(t2 + t3)*cos(t5) + 44*cos(t2)*cos(t3)*cos(t4)*sin(t5) ...
           - 44*cos(t4)*sin(t2)*sin(t3)*sin(t5)));

    J(1,3) = round(-5*cos(t1)*(130*cos(t2)*sin(t3) - 21*cos(t2)*cos(t3) ...
           + 130*cos(t3)*sin(t2) + 21*sin(t2)*sin(t3) + 44*sin(t2 + t3)*cos(t5) ...
           + 44*cos(t2)*cos(t3)*cos(t4)*sin(t5) - 44*cos(t4)*sin(t2)*sin(t3)*sin(t5)));

    J(1,4) = round(220*sin(t5)*(cos(t1)*cos(t2)*sin(t3)*sin(t4) ...
           - cos(t4)*sin(t1) + cos(t1)*cos(t3)*sin(t2)*sin(t4)));

    J(1,5) = round(-220*cos(t5)*sin(t1)*sin(t4) - 220*cos(t2 + t3)*cos(t1)*sin(t5) ...
           - 220*cos(t1)*cos(t2)*cos(t4)*cos(t5)*sin(t3) ...
           - 220*cos(t1)*cos(t3)*cos(t4)*cos(t5)*sin(t2));

    J(1,6) = 0;

    J(2,1) = round(525*cos(t1)*sin(t2) - 650*cos(t1)*sin(t2)*sin(t3) ...
           - 220*sin(t1)*sin(t4)*sin(t5) + 220*cos(t2 + t3)*cos(t1)*cos(t5) ...
           + 650*cos(t1)*cos(t2)*cos(t3) + 105*cos(t1)*cos(t2)*sin(t3) ...
           + 105*cos(t1)*cos(t3)*sin(t2) - 220*cos(t1)*cos(t2)*cos(t4)*sin(t3)*sin(t5) ...
           - 220*cos(t1)*cos(t3)*cos(t4)*sin(t2)*sin(t5));

    J(2,2) = round(-5*sin(t1)*(130*cos(t2)*sin(t3) - 21*cos(t2)*cos(t3) ...
           - 105*cos(t2) + 130*cos(t3)*sin(t2) + 21*sin(t2)*sin(t3) ...
           + 44*sin(t2 + t3)*cos(t5) + 44*cos(t2)*cos(t3)*cos(t4)*sin(t5) ...
           - 44*cos(t4)*sin(t2)*sin(t3)*sin(t5)));

    J(2,3) = round(-5*sin(t1)*(130*cos(t2)*sin(t3) - 21*cos(t2)*cos(t3) ...
           + 130*cos(t3)*sin(t2) + 21*sin(t2)*sin(t3) + 44*sin(t2 + t3)*cos(t5) ...
           + 44*cos(t2)*cos(t3)*cos(t4)*sin(t5) - 44*cos(t4)*sin(t2)*sin(t3)*sin(t5)));

    J(2,4) = round(220*sin(t5)*(cos(t1)*cos(t4) + cos(t2)*sin(t1)*sin(t3)*sin(t4) ...
           + cos(t3)*sin(t1)*sin(t2)*sin(t4)));

    J(2,5) = round(-220*cos(t1)*cos(t5)*sin(t4) - 220*cos(t2 + t3)*sin(t1)*sin(t5) ...
           - 220*cos(t2)*cos(t4)*cos(t5)*sin(t1)*sin(t3) ...
           - 220*cos(t3)*cos(t4)*cos(t5)*sin(t1)*sin(t2));

    J(2,6) = 0;

    J(3,1) = 0;

    J(3,2) = round(110*sin(t2 + t3)*sin(t4 + t5) - 105*sin(t2 + t3) ...
           - 525*sin(t2) - 110*sin(t4 - t5)*sin(t2 + t3) - 650*cos(t2 + t3) ...
           - 220*cos(t2 + t3)*cos(t5));

    J(3,3) = round(220*sin(t2 + t3)*cos(t4)*sin(t5) - 105*sin(t2 + t3) ...
           - 220*cos(t2 + t3)*cos(t5) - 650*cos(t2 + t3));

    J(3,4) = round(220*cos(t2 + t3)*sin(t4)*sin(t5));

    J(3,5) = round(220*sin(t2 + t3)*sin(t5) - 220*cos(t2 + t3)*cos(t4)*cos(t5));

    J(3,6) = 0;

    Jm = pinv(J);
    % Jm = inv((J')*J)*(J');

end
